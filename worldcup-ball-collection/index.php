        		<h2><a href="http://www.soccer.com/guide/fifa-world-cup-in-review-2010-south-africa">South Africa 2010: Jabulani</a></h2>
            	<p>After departing from the 32 panels in the previous World Cup, adidas went with even fewer panels this time, just eight thermally bonded and molded this time. adidas also introduced "Grip 'n' Groove" technology, a textured surface to improve ball flight. Goalkeepers complained about the dip and swerve the ball facilitated. Goalscorers reveled. The design used 11 different colors, a nod to the 11 players on the field and the 11 languages spoken in South Africa. Jabulani means, "to celebrate," in Zulu. A gold version was  used in the final, again.</p>
