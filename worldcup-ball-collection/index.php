            	<p>adidas set a new trend with the Tango in introducing "triads," a graphic of 20 panels that together formed a dozen identical circles, though the ball was actually made of 32 panels. Inspired by the elegance, passion and emotion of Argentines, the Tango and its triad design would go on to be the basis for the next five World Cup balls and also be used at the Olympics and UEFA Euro 1980.</p>
