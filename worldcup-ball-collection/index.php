            	<p>Out with the Aztec design, in with the fine art of the Etruscans of ancient Italy. Three Estruscan lion heads adorned each of the triads. Internal polyurethane foam created a lighter ball that rebounded well, and the synthetic fiber manufacturing prevented tearing while keeping the ball tight and free from being waterlogged.</p>
