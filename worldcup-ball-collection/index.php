            	<p>The Tango's patented triad design is abandoned in favor of an Asian-inspired golden orb with red flames, a nod to South Korea and Japan's effort and energy to host the event. Extremely lightweight, the ball featured a three-layer knitted chassis with even energy distribution at each point to give players accuracy when making long passes.</p>
