            	<p>The "quest of the stars" ball had an otherworldly inspired triad design and improvements in acceleration, water prevention, softness and ability to be controlled. Made from five materials and wrapped in polystyrene foam, the Questra was tighter, lighter and more responsive. It took its inspiration from the Apollo 11 mission and high-speed rockets.</p>
