        		<h2><a href="http://www.soccer.com/guide/fifa-world-cup-in-review-2006-germany">Germany 2006: +Teamgeist</a></h2>
            	<p>The +Teamgeist, meaning "team spirit" in German, used glue instead of hand stitching and 14 curved panels instead of the 32 that had been in place since 1970. The new panel design created a rounder, more predictable ball. adidas also innovated in printing the match date, teams, stadium name and kickoff time for each contest. The ball for the final was decorated with gold accents. It was the first thermally bonded World Cup ball.</p>
