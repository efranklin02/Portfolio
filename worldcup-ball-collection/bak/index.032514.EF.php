                    <section id="westgermany" name="westgermany">
                        <h2>West Germany 1974: Telstar Durlast</h2>
                        <img src="images/mainwestgermany.png" alt="West Germany 1974: Telstar Durlast" />
                        <p>The Durlast lasts awhile longer. adidas did a good thing. FIFA stuck with it. Other than the addition of text—the name of the ball, event, manufacturer and where its made—they didn't mess with success, staying with the same materials and process. They also made an all-white version of the Telstar, the adidas Chile, as an homage to the ball used in World Cup 1962.<span><a href="http://www.soccer.com/guide/fifa-world-cup-in-review-1974-west-germany/">+ LEARN MORE</a></span></p>
                    </section>
