                        <h2>Argentina 1978: Tango Durlast</h2>
                        <img src="images/mainargentina.png" alt="Argentina 1978: Tango Durlast" />
                        <p>adidas set a new trend with the Tango in introducing "triads," a graphic of 20 panels that together formed a dozen identical circles, though the ball was actually made of 32 panels. Inspired by the elegance, passion and emotion of Argentines, the Tango and its triad design would go on to be the basis for the next five World Cup balls and also be used at the Olympics and UEFA Euro 1980.<span><a href="http://www.soccer.com/guide/fifa-world-cup-in-review-1978-argentina/">+ LEARN MORE</a></span></p>
                    </section>
                    <section id="spain" name="spain"> 
                        <h2>Spain 1982: Tango Espa&ntilde;a</h2>
                        <img src="images/mainspain.png" alt="Spain 1982: Tango Espa&ntilde;a" />
                        <p>The new Tango was more water-resistant thanks to a polyurethane coat and rubber on the seams which played bouncer to water's wishes to seep into the core. The Tango España is also historic because it's the last time the World Cup was contested with a ball made of 100 percent genuine leather.<span><a href="http://www.soccer.com/guide/fifa-world-cup-in-review-1982-spain">+ LEARN MORE</a></span></p>
                    </section>
                    </div>
                    
                    <div class="section-container">
                    <section id="mexico86" name="mexico86">
                        <h2>Mexico 1986: Azteca Mexico</h2>
                        <img src="images/mainMexico86.png" alt="Mexico 1986: Azteca Mexico" />
                        <p>Enter the synthetic, polyurethane-coated ball and its rain resistance. The triads endured, and the circles were decorated in Aztec style, a nice touch. The ball was a massive leap forward in its ability to stay dry and maintain its shape on a hard ground or at high altitude.<span><a href="http://www.soccer.com/guide/fifa-world-cup-in-review-1986-mexico">+ LEARN MORE</a></span></p>
                    </section>
                    <section id="italy" name="italy">
                        <h2>Italy 1990: Etrusco Unico</h2>
                        <img src="images/mainitaly.png" alt="Italy 1990: Etrusco Unico" />
                        <p>Out with the Aztec design, in with the fine art of the Etruscans of ancient Italy. Three Estruscan lion heads adorned each of the triads. Internal polyurethane foam created a lighter ball that rebounded well, and the synthetic fiber manufacturing prevented tearing while keeping the ball tight and free from being waterlogged.<span><a href="http://www.soccer.com/guide/fifa-world-cup-in-review-1990-italy">+ LEARN MORE</a></span></p>
                    </section>
                    </div>
                    
                    <div class="section-container">
                    <section id="usa" name="usa">
                        <h2>USA 1994: Questra</h2>
                        <img src="images/mainusa.png" alt="USA 1994: Questra" />
                        <p>The "quest of the stars" ball had an otherworldly inspired triad design and improvements in acceleration, water prevention, softness and ability to be controlled. Made from five materials and wrapped in polystyrene foam, the Questra was tighter, lighter and more responsive. It took its inspiration from the Apollo 11 mission and high-speed rockets.<span><a href="http://www.soccer.com/guide/fifa-world-cup-in-review-1994-united-states">+ LEARN MORE</a></span></p>
                    </section>
                    <section id="france" name="france">
                        <h2>France 1998: EQT Tricolore</h2>
                        <img src="images/mainfrance.png" alt="France 1998: EQT Tricolore" />
                        <p>You probably guessed it from the name, but this is the first multi-colored—red, white and blue to honor France-ball in World Cup history. Gas-filled bubbles inside afforded a supple touch. The ball was even softer and faster than the Questra thanks to a new foam layer. It was the first ball made outside of Europe, having been produced in Morocco and Indonesia.<span><a href="http://www.soccer.com/guide/fifa-world-cup-in-review-1998-france">+ LEARN MORE</a></span></p>
                    </section>
                    </div>
                    
                    <div class="section-container">
                    <section id="southkorea" name="southkorea">
                        <h2>South Korea, Japan 2002: Fevernova</h2>
                        <img src="images/mainsouthkorea.png" alt="South Korea, Japan 2002: Fevernova" />
                        <p>The Tango's patented triad design is abandoned in favor of an Asian-inspired golden orb with red flames, a nod to South Korea and Japan's effort and energy to host the event. Extremely lightweight, the ball featured a three-layer knitted chassis with even energy distribution at each point to give players accuracy when making long passes.<span><a href="http://www.soccer.com/guide/fifa-world-cup-in-review-2002-south-koreajapan">+ LEARN MORE</a></span></p>
                    </section>
                    <section id="germany" name="germany">
                        <h2>Germany 2006: +Teamgeist</h2>
                        <img src="images/maingermany.png" alt="Germany 2006: +Teamgeist" />
                        <p>The +Teamgeist, meaning "team spirit" in German, used glue instead of hand stitching and 14 curved panels instead of the 32 that had been in place since 1970. The new panel design created a rounder, more predictable ball. adidas also innovated in printing the match date, teams, stadium name and kickoff time for each contest. The ball for the final was decorated with gold accents. It was the first thermally bonded World Cup ball.<span><a href="http://www.soccer.com/guide/fifa-world-cup-in-review-2006-germany">+ LEARN MORE</a></span></p>
                    </section>
                    </div>
                    
                    <section id="southafrica" name="southafrica">
                        <h2>South Africa 2010: Jabulani</h2>
                        <img src="images/mainsouthafrica.png" alt="South Africa 2010: Jabulani" />
                        <p>After departing from the 32 panels in the previous World Cup, adidas went with even fewer panels this time, just eight thermally bonded and molded this time. adidas also introduced "Grip 'n' Groove" technology, a textured surface to improve ball flight. Goalkeepers complained about the dip and swerve the ball facilitated. Goalscorers reveled. The design used 11 different colors, a nod to the 11 players on the field and the 11 languages spoken in South Africa. Jabulani means, "to celebrate," in Zulu. A gold version was  used in the final, again.<span><a href="http://www.soccer.com/guide/fifa-world-cup-in-review-2010-south-africa">+ LEARN MORE</a></span></p>
