                        <h2>Mexico 1970: Telstar Durlast</h2>
                        <img src="images/mainMexico.png" alt="Mexico 1970: Telstar Durlast" />
                        <div class="textBG">
                        <p>adidas got involved, making its first World Cup sphere seven years after it started building balls. For their first act, adidas developed the iconic black-and-white paneled soccer ball. It was the first multicolor ball used in the event and was intended the catch the eyes of TV viewers. The FIFA World Cup 1970 was the first to be broadcast on live television, and the black and white panels catered to pre-color televisions. The black pentagons also helped players recognize the swerve and flight of the ball.<span><a href="http://www.soccer.com/guide/world-cup-in-review-1970-mexico/">+ LEARN MORE</a></span></p>
                    	</div>
